<h1 class="text-center">Política de privacidad</h1>

Esta Política de Privacidad describe cómo recopilamos, utilizamos, protegemos y compartimos la información personal de los usuarios que visitan nuestra página de noticias de videojuegos con una sección de venta de productos. Al utilizar nuestros servicios, aceptas las prácticas descritas en esta política.

<h2 class="mt-5">1. Información que Recopilamos</h2>
<p>
    1.1. Información Personal: Podemos recopilar información personal identificable, como nombre, dirección de correo electrónico, información de contacto y datos de facturación, cuando los usuarios crean una cuenta en nuestro sitio web o realizan una compra en la sección de venta de productos.
</p>
<p>
    1.2. Información de Uso: Recopilamos información sobre el uso de nuestra página, incluyendo direcciones IP, tipo de navegador, páginas visitadas, tiempo de navegación y otros datos de uso. Esta información se recopila de forma anónima y se utiliza para mejorar nuestros servicios y la experiencia del usuario.
</p>


<h2 class="mt-5">2. Uso de la Información</h2>
<p>
    <p>2.1. Utilizamos la información personal recopilada para:</p>

    Proporcionar y gestionar nuestros servicios, incluyendo la creación de cuentas de usuario y el procesamiento de transacciones de venta.
    Enviar comunicaciones relacionadas con el uso de nuestros servicios, como confirmaciones de compra, actualizaciones o cambios en nuestros términos y condiciones.
    Personalizar la experiencia del usuario y ofrecer contenido y recomendaciones relevantes basadas en sus intereses y preferencias.
    Mejorar la seguridad y protección de nuestro sitio web y de los usuarios.
</p>
<p>
    2.2. No compartimos información personal con terceros, excepto cuando sea necesario para cumplir con la ley, responder a procesos legales o proteger nuestros derechos, propiedad o seguridad.
</p>

<h2 class="mt-5">3. Seguridad de la Información</h2>
<p>
    3.1. Implementamos medidas de seguridad adecuadas para proteger la información personal de los usuarios y prevenir el acceso no autorizado, la divulgación, la alteración o la destrucción de dicha información.
</p>  
<p>
    3.2. Sin embargo, no podemos garantizar la seguridad absoluta de la información transmitida a través de Internet. Los usuarios asumen el riesgo de compartir información personal en línea y deben tomar precauciones para proteger su propia información.    
</p>  

<h2 class="mt-5">4. Cookies y Tecnologías Similares</h2>
<p>
    4.1. Utilizamos cookies y tecnologías similares para recopilar información sobre el uso de nuestra página y personalizar la experiencia del usuario. Las cookies son pequeños archivos de texto almacenados en el dispositivo del usuario. Puedes ajustar la configuración de tu navegador para rechazar cookies o para que se te notifique cuando se envíen cookies.
</p>

<h2 class="mt-5">5. Cambios en la Política de Privacidad</h2>
<p>
    5.1. Nos reservamos el derecho de modificar esta Política de Privacidad en cualquier momento. Cualquier cambio se publicará en nuestra página y será efectivo inmediatamente. Se recomienda a los usuarios revisar periódicamente esta política para estar informados sobre cómo protegemos la información personal.
</p>

<h2 class="mt-5">6. Contacto</h2>
<p>
    6.1. Si tienes alguna pregunta, inquietud o solicitud relacionada con esta Política de Privacidad, puedes ponerte en contacto con nosotros a través de la información de contacto proporcionada en nuestro sitio  
</p>