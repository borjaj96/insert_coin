<h1 class="text-center">Preguntas frecuentes</h1>

<h2 class="mt-5">1. ¿Qué tipo de noticias puedo encontrar en vuestra página de noticias de videojuegos?</h2>

En nuestra página de noticias de videojuegos, cubrimos una amplia variedad de temas relacionados con la industria de los videojuegos. Puedes encontrar noticias sobre lanzamientos de nuevos juegos, actualizaciones de juegos existentes, avances y tráilers, eventos y convenciones de videojuegos, entrevistas con desarrolladores y mucho más.

<h2 class="mt-5">2. ¿Cómo puedo vender mis productos de videojuegos en vuestra página?</h2>

Tenemos una sección dedicada a la venta de productos de videojuegos donde los usuarios pueden vender sus juegos y accesorios. Para vender un producto, sigue estos pasos:

Crea una cuenta en nuestro sitio web si aún no lo has hecho.
Accede a tu cuenta y ve a la sección de venta de productos.
Haz clic en el botón "Vender" y completa la información requerida sobre el producto que deseas vender, incluyendo detalles, precio y fotos.
Revisa la información y confirma la publicación del producto.
Los usuarios interesados ​​en comprar tu producto podrán contactarte a través de nuestra plataforma para finalizar la transacción.

<h2 class="mt-5">3. ¿Cuánto cuesta vender un producto en vuestra página?</h2>

No cobramos tarifas por publicar tus productos en nuestra sección de venta. Sin embargo, si se realiza una venta exitosa, aplicaremos una pequeña comisión como porcentaje del precio de venta. Los detalles sobre las comisiones se proporcionarán durante el proceso de publicación del producto.

<h2 class="mt-5">4. ¿Cuáles son los requisitos para vender productos en vuestra página?</h2>

Para vender productos en nuestra página, debes cumplir con los siguientes requisitos:

Debes tener al menos 18 años de edad o contar con el consentimiento de un tutor legal.
Debes proporcionar información precisa y completa sobre los productos que deseas vender.
Los productos deben estar en condiciones adecuadas y funcionales, y deben cumplir con las leyes y regulaciones aplicables.

<h2 class="mt-5">5. ¿Cómo puedo contactar al vendedor si tengo preguntas sobre un producto en venta?</h2>

Si tienes preguntas sobre un producto en venta, puedes utilizar nuestra plataforma de mensajería interna para contactar al vendedor directamente. Solo debes hacer clic en el botón "Contactar al vendedor" en la página del producto y podrás enviar un mensaje al vendedor.

<h2 class="mt-5">6. ¿Cuánto tiempo tarda en aprobarse la publicación de un producto en la sección de venta?</h2>

Hacemos todo lo posible para revisar y aprobar las publicaciones de productos en un plazo de 24 a 48 horas. Sin embargo, este tiempo puede variar dependiendo del volumen de publicaciones y otros factores. Te recomendamos ser paciente y asegurarte de que toda la información proporcionada sea precisa y completa para agilizar el proceso de aprobación.

<h2 class="mt-5">7. ¿Ofrecéis algún tipo de garantía para los productos comprados en vuestra página?</h2>

Como plataforma de venta de productos de terceros, no ofrecemos garantía directa para los productos vendidos en nuestra página. Sin embargo, animamos a los vendedores a proporcionar descripciones precisas y detalladas de los productos, así como a responder cualquier pregunta adicional que los compradores puedan tener antes de realizar una compra. Además, recomendamos a los compradores revisar la información del vendedor y las calificaciones de otros usuarios antes de realizar una compra.